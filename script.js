		//afficher les informations sur les experts en empreinte
function affichertrace() {
  if (document.getElementById("trace").style.display == "block") {
      document.getElementById("trace").style.display = "none";
  }
  else {
      document.getElementById("trace").style.display = "block";
  }
}

function affichertrace2() {
  document.getElementById("canvtrace").style.border = "1px solid black";
}

function affichertrace3() {
  document.getElementById("canvtrace").style.border = "none";
}



